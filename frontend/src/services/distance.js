const ROAD_FACTOR = 1.27; // приближение пробега по трассе к расстоянию по прямой

function normalizeCity(name) {
  return String(name || '')
    .trim()
    .toLowerCase()
    .replace(/ё/g, 'е')
    .replace(/\s+/g, ' ');
}

function haversineKm([lat1, lon1], [lat2, lon2]) {
  const R = 6371;
  const toRad = d => (d * Math.PI) / 180;
  const dLat = toRad(lat2 - lat1);
  const dLon = toRad(lon2 - lon1);
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos(toRad(lat1)) * Math.cos(toRad(lat2)) * Math.sin(dLon / 2) ** 2;
  return 2 * R * Math.asin(Math.sqrt(a));
}

/**
 * Возвращает ориентировочное расстояние между городами (км) или null,
 * если хотя бы один город отсутствует в справочнике координат.
 */
export function estimateDistanceKm(originCity, destCity) {
  const a = CITY_COORDS[normalizeCity(originCity)];
  const b = CITY_COORDS[normalizeCity(destCity)];
  if (!a || !b) return null;
  const km = haversineKm(a, b) * ROAD_FACTOR;
  return Math.round(km / 5) * 5; // округление до 5 км
}

export function hasCityCoords(city) {
  return Boolean(CITY_COORDS[normalizeCity(city)]);
}
